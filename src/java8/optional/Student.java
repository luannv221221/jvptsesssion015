package java8.optional;

public class Student {
    private int studentCode;

    public Student() {
    }

    public Student(int studentCode) {
        this.studentCode = studentCode;
    }

    public int getStudentCode() {
        return studentCode;
    }

    public void setStudentCode(int studentCode) {
        this.studentCode = studentCode;
    }
}
